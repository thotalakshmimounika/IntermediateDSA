# You are given the head of a linked list A and an integer B, check if there is any node which contains this value B.

# Return 1 if such a node is present else return 0.



# Problem Constraints
# 1 <= size of linked list <= 105
# 1 <= value of nodes <= 109
# 1 <= B <= 109



# Input Format
# The first argument A is the head of a linked list.

# The second arguement B is an integer.



# Output Format
# Return 1 if such a node is present otherwise return 0.


# Example Input
# Input 1:
# A = 1 -> 2 -> 3
# B = 4
# Input 2:
# A = 4 -> 3 -> 2 -> 1
# B = 4


# Example Output
# Output 1:
# 0
# Output 2:
# 1


# Example Explanation
# For Input 1:
# None of the nodes have a value 4.
# For Input 2:
# The first node has a value 4.
# Definition for singly-linked list.
# class ListNode:
#    def __init__(self, x):
#        self.val = x
#        self.next = None

class Solution:
    # @param A : head node of linked list
    # @param B : integer
    # @return an integer
    def solve(self, a,b):
        temp=a
        while(temp!=None):
            if temp.val==b:
                return 1
            temp=temp.next
        return 0
